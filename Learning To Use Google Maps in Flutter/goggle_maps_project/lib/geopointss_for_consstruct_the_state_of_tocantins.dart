List<GeoPoint> convertToGeoPoints(List<List<double>> data) {
  List<GeoPoint> geoPoints = [];
  for (var item in data) {
    double longitude = item[0];
    double latitude = item[1];
    geoPoints.add(GeoPoint(latitude: latitude, longitude: longitude));
  }
  return geoPoints;
}

